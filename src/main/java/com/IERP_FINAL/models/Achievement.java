package com.IERP_FINAL.models;
public class Achievement {
    private int achievementId;

    private int studentId;
    private String activityType;
    private String title;
    private String organization;
    private int numberOfDays;
    private String fromDate;
    private String toDate;
    private String description;
    private byte[] testimonialImage;

    // Constructor
    public Achievement() {
    }
    
    // Getters and Setters
    public int getAchievementId() {
        return achievementId;
    }

    public void setAchievementId(int achievementId) {
        this.achievementId = achievementId;
    }


    // Getters and Setters
    public int getStudentId() {
        return studentId;
    }

    public void setStudentId(int studentId) {
        this.studentId = studentId;
    }

    public String getActivityType() {
        return activityType;
    }

    public void setActivityType(String activityType) {
        this.activityType = activityType;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getOrganization() {
        return organization;
    }

    public void setOrganization(String organization) {
        this.organization = organization;
    }

    public int getNumberOfDays() {
        return numberOfDays;
    }

    public void setNumberOfDays(int numberOfDays) {
        this.numberOfDays = numberOfDays;
    }

    public String getFromDate() {
        return fromDate;
    }

    public void setFromDate(String fromDate) {
        this.fromDate = fromDate;
    }

    public String getToDate() {
        return toDate;
    }

    public void setToDate(String toDate) {
        this.toDate = toDate;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public byte[] getTestimonialImage() {
        return testimonialImage;
    }

    public void setTestimonialImage(byte[] testimonialImage) {
        this.testimonialImage = testimonialImage;
    }
}
